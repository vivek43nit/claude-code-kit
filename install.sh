#!/usr/bin/env bash
# install.sh — Bootstrap a project to use claude-code-kit
#
# Usage:
#   bash install.sh /path/to/your/project
#   bash install.sh .   (installs into current directory)
#
# What it does:
#   1. Copies .claude/hooks/ to target project
#   2. Copies guidelines/ (language guideline files) to target project
#   3. Merges plugin config into target .claude/settings.json
#   4. Prints CI template copy instructions

set -euo pipefail

SOURCE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
TARGET="${1:-}"

if [ -z "$TARGET" ]; then
    echo "Usage: bash install.sh /path/to/your/project"
    echo "       bash install.sh .  (current directory)"
    exit 1
fi

TARGET="$(cd "$TARGET" && pwd)"

if [ "$TARGET" = "$SOURCE_DIR" ]; then
    echo "Target is the same as source — nothing to do."
    exit 0
fi

echo "Installing claude-code-kit into: $TARGET"
echo ""

# ── 1. Copy hooks ──────────────────────────────────────────────────────────

HOOKS_SRC="$SOURCE_DIR/.claude/hooks"
HOOKS_DST="$TARGET/.claude/hooks"

mkdir -p "$HOOKS_DST"

for f in "$HOOKS_SRC"/*.sh; do
    name=$(basename "$f")
    if [ -f "$HOOKS_DST/$name" ]; then
        echo "  [SKIP] $HOOKS_DST/$name already exists (not overwriting)"
    else
        cp "$f" "$HOOKS_DST/$name"
        chmod +x "$HOOKS_DST/$name"
        echo "  [OK]   Copied hook: .claude/hooks/$name"
    fi
done

# ── 2. Copy guidelines ────────────────────────────────────────────────────

GUIDE_SRC="$SOURCE_DIR/guidelines"
GUIDE_DST="$TARGET/guidelines"

mkdir -p "$GUIDE_DST"

for f in "$GUIDE_SRC"/*.md; do
    name=$(basename "$f")
    [ "$name" = "active.md" ] && continue  # Skip generated file
    if [ -f "$GUIDE_DST/$name" ]; then
        echo "  [SKIP] $GUIDE_DST/$name already exists (not overwriting)"
    else
        cp "$f" "$GUIDE_DST/$name"
        echo "  [OK]   Copied guideline: guidelines/$name"
    fi
done

# Seed an empty active.md
if [ ! -f "$GUIDE_DST/active.md" ]; then
    echo "# Active Language Guidelines" > "$GUIDE_DST/active.md"
    echo "<!-- Auto-generated — run a Claude Code session to populate -->" >> "$GUIDE_DST/active.md"
    echo "  [OK]   Created guidelines/active.md (empty — populated on first session)"
fi

# ── 3. Merge settings.json ────────────────────────────────────────────────

SETTINGS_DST="$TARGET/.claude/settings.json"
SETTINGS_SRC="$SOURCE_DIR/.claude/settings.json"

mkdir -p "$TARGET/.claude"

if [ ! -f "$SETTINGS_DST" ]; then
    cp "$SETTINGS_SRC" "$SETTINGS_DST"
    echo "  [OK]   Created .claude/settings.json"
else
    echo "  [WARN] .claude/settings.json already exists."
    echo "         Manually merge hooks from: $SETTINGS_SRC"
    echo "         Hooks to add:"
    echo '           "UserPromptSubmit": [{"hooks": [{"type": "command", "command": "bash .claude/hooks/detect-languages.sh"}]}]'
    echo '           "PreToolUse" (matcher Write|Edit): [{"hooks": [{"type": "command", "command": "bash .claude/hooks/security-scan.sh"}]}]'
fi

# ── 4. Add active.md to .gitignore ────────────────────────────────────────

GITIGNORE="$TARGET/.gitignore"
if [ -f "$GITIGNORE" ]; then
    if ! grep -q "guidelines/active.md" "$GITIGNORE"; then
        echo "" >> "$GITIGNORE"
        echo "# Auto-generated by claude-code-kit" >> "$GITIGNORE"
        echo "guidelines/active.md" >> "$GITIGNORE"
        echo "  [OK]   Added guidelines/active.md to .gitignore"
    else
        echo "  [SKIP] guidelines/active.md already in .gitignore"
    fi
else
    echo "guidelines/active.md" > "$GITIGNORE"
    echo "  [OK]   Created .gitignore with guidelines/active.md"
fi

# ── 5. CI instructions ────────────────────────────────────────────────────

echo ""
echo "────────────────────────────────────────────────────────"
echo "CI Templates (copy to your project):"
echo ""
echo "  GitHub Actions:"
echo "    mkdir -p $TARGET/.github/workflows"
echo "    cp $SOURCE_DIR/ci/github/quality-gates.yml $TARGET/.github/workflows/quality-gates.yml"
echo ""
echo "  GitLab CI:"
echo "    cp $SOURCE_DIR/ci/gitlab/quality-gates.yml $TARGET/.gitlab-ci.yml"
echo ""
echo "────────────────────────────────────────────────────────"
echo ""
echo "Done! Next steps:"
echo "  1. Copy the CI template above"
echo "  2. Open Claude Code in $TARGET"
echo "  3. Run /reload-plugins"
echo "  4. Start a session — languages will auto-detect and populate guidelines/active.md"
